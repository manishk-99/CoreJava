package Pack2;

public class Student {
    private int rollno;
    private String name;

    public void setRoll(int r)
    {
        rollno = r;
    }
    public void setName(String n)
    {
        name = n;
    }
    public int getRoll()
    {
        return rollno;
    }
    public String getName()
    {
        return name;
    }
}
